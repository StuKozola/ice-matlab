classdef LatestPerSourceIdTest < matlab.unittest.TestCase

    methods (Test)
        function picksNewestPerFtpcsd(tc)
            in = [ ...
                "FTPCSD_PUB1_558_20260508.csv.bz2", ...
                "FTPCSD_PUB1_558_20260509.csv.bz2", ...
                "FTPCSD_PUB1_558_20260510.csv.bz2", ...
                "FTPCSD_PUB1_558_20260511.csv.bz2", ...
                "FTPCSD_PUB1_559_20260510.csv.bz2", ...
                "FTPCSD_PUB1_559_20260511.csv.bz2", ...
                "FTPCSD_PUB1_708_20260508.csv.bz2"];
            out = ice.ftp.latestPerSourceId(in, "FTPCSD");
            tc.verifyEqual(sort(out), sort([ ...
                "FTPCSD_PUB1_558_20260511.csv.bz2", ...
                "FTPCSD_PUB1_559_20260511.csv.bz2", ...
                "FTPCSD_PUB1_708_20260508.csv.bz2"].'));
        end

        function ignoresLatestSymlinkAndOtherNoise(tc)
            in = [ ...
                "FTPCSD_PUB1_558.csv.bz2", ...        % dateless symlink, ignored
                "FTPCSD_PUB1.csv.bz2", ...            % collection symlink, ignored
                "Eodd2ReadmeRef.txt", ...             % unrelated file
                "FTPCSD_PUB1_558_20260511.csv.bz2"];  % real file
            out = ice.ftp.latestPerSourceId(in, "FTPCSD");
            tc.verifyEqual(out, "FTPCSD_PUB1_558_20260511.csv.bz2");
        end

        function legacyRootLayoutMatched(tc)
            in = [ ...
                "FTPCSD_558_20260510.csv.gz", ...
                "FTPCSD_558_20260511.csv.gz", ...
                "FTPCSD_564_20260511.csv.gz"];
            out = ice.ftp.latestPerSourceId(in, "FTPCSD");
            tc.verifyEqual(sort(out), sort([ ...
                "FTPCSD_558_20260511.csv.gz", ...
                "FTPCSD_564_20260511.csv.gz"].'));
        end

        function ftpsedolSingleNewest(tc)
            in = [ ...
                "FTPSEDOL_PUB5_20260508.csv.bz2", ...
                "FTPSEDOL_PUB5_20260509.csv.bz2", ...
                "FTPSEDOL_PUB5_20260510.csv.bz2", ...
                "FTPSEDOL_PUB5_20260511.csv.bz2", ...
                "FTPSEDOL_PUB5.csv.bz2"];     % "latest" symlink
            out = ice.ftp.latestPerSourceId(in, "FTPSEDOL");
            tc.verifyEqual(out, "FTPSEDOL_PUB5_20260511.csv.bz2");
        end

        function emptyInputReturnsEmpty(tc)
            tc.verifyEqual(ice.ftp.latestPerSourceId(strings(0), "FTPCSD"), strings(0));
        end

        function unknownProductErrors(tc)
            tc.verifyError( ...
                @() ice.ftp.latestPerSourceId("x", "WHATEVER"), ...
                "ice:ftp:latestPerSourceId:UnknownProduct");
        end
    end
end
